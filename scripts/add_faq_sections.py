FAQ_HTML_TEMPLATE = """
        <!-- よくある質問セクション -->
        <div style="margin-top: 32px; margin-bottom: 32px;">
            <h2 style="color: #2c3e50; font-size: 18px; font-weight: 800; margin-bottom: 16px; border-bottom: 2px solid #e2e7ea; padding-bottom: 8px;">
                {ward_name}の保育園 よくある質問
            </h2>
            <div style="display: flex; flex-direction: column; gap: 12px;">
{faq_items}
            </div>
        </div>
"""

FAQ_ITEM_TEMPLATE = """                <div style="border: 1px solid #e2e7ea; border-radius: 8px; overflow: hidden;">
                    <div style="background: #f8f9fa; padding: 12px 16px; font-size: 14px; font-weight: 700; color: #2c3e50; display: flex; gap: 8px; align-items: flex-start;">
                        <span style="color: #2d7cbc; font-weight: 900; flex-shrink: 0;">Q</span>
                        <span>{question}</span>
                    </div>
                    <div style="padding: 12px 16px; font-size: 13px; color: #555; line-height: 1.8; display: flex; gap: 8px; align-items: flex-start;">
                        <span style="color: #e67e22; font-weight: 900; flex-shrink: 0;">A</span>
                        <span>{answer}</span>
                    </div>
                </div>"""


def build_faq_html(ward_name, faqs):
    items = "\n".join(
        FAQ_ITEM_TEMPLATE.format(question=f["q"], answer=f["a"])
        for f in faqs
    )
    return FAQ_HTML_TEMPLATE.format(ward_name=ward_name, faq_items=items)


def process_ward_file(filepath, ward_key):
    info = WARD_FAQ[ward_key]
    faq_html = build_faq_html(info["ward_name"], info["faqs"])

    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    # 既存のFAQセクションがあればスキップ
    if "よくある質問セクション" in content:
        print(f"  SKIP (already has FAQ): {filepath}")
        return False

    # 「次にやること」セクションの直前に挿入
    marker = "        <!-- 空き状況を見た後の次のアクション -->"
    if marker not in content:
        print(f"  ERROR: marker not found in {filepath}")
        return False

    content = content.replace(marker, faq_html + "\n" + marker)

    with open(filepath, "w", encoding="utf-8") as f:
        f.write(content)

    print(f"  OK: {filepath}")
    return True


if __name__ == "__main__":
    import os

    base_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    print(f"Base dir: {base_dir}")

    for ward_key in WARD_FAQ:
        filepath = os.path.join(base_dir, f"{ward_key}.html")
        if os.path.exists(filepath):
            process_ward_file(filepath, ward_key)
        else:
            print(f"  NOT FOUND: {filepath}")
